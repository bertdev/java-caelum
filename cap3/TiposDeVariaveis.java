class TiposDeVariaveis {
  public static void main(String[] args) {
    //armazenar numeros inteiros
    int idade = 20;
    System.out.println(idade);

    //armazenar numeros reais(frações, numeros inteiros e outros)
    double pi = 3.14;
    System.out.println(pi);

    //armazenar booleanos(true e false)
    //operações booleandas retornam booleanos como resultado: 10 < 5 -> false 
    boolean verdade = true;
    System.out.println(verdade);

    //armazenar UM unico caractere 
    //o tipo char precisa de aspas simples, não considera vazio ('') um caracter 
    char vogalInicial = 'a';
    System.out.println(vogalInicial);

    //esses sõa os tipos primitivos do java, alem desses exitem
    //byte ,short, long e float. a atribuição de tipos primitivos a uma variavel
    //ocorre por valor e não por referência

  }
}
